public class Weapon {
    private String coldWeapons;

    private String firearms;

    private String laserWeapons;

    public String getColdWeapons() {
        return coldWeapons;
    }

    public void setColdWeapons(String coldWeapons) {
        this.coldWeapons = coldWeapons;
    }

    public String getFirearms() {
        return firearms;
    }

    public void setFirearms(String firearms) {
        this.firearms = firearms;
    }

    public String getLaserWeapons() {
        return laserWeapons;
    }

    public void setLaserWeapons(String laserWeapons) {
        this.laserWeapons = laserWeapons;
    }
}
